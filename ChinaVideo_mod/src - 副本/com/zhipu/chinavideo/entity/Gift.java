package com.zhipu.chinavideo.entity;

public class Gift {
	private String site;
	private String id;
	private String icon;
	private String category;
	private String price;
	private String effect;
	private String description;
	private String sendmessage;
	private String name;
	private String received_beans;
	private String alt_price;
	private String bind;
	private String sort_index;

	public Gift(String site, String id, String icon, String category,
			String price, String effect, String description,
			String sendmessage, String name, String received_beans,
			String alt_price, String bind, String sort_index) {
		super();
		this.site = site;
		this.id = id;
		this.icon = icon;
		this.category = category;
		this.price = price;
		this.effect = effect;
		this.description = description;
		this.sendmessage = sendmessage;
		this.name = name;
		this.received_beans = received_beans;
		this.alt_price = alt_price;
		this.bind = bind;
		this.sort_index = sort_index;
	}

	public Gift() {
		super();
	}

	public String getSite() {
		return site;
	}

	public void setSite(String site) {
		this.site = site;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getIcon() {
		return icon;
	}

	public void setIcon(String icon) {
		this.icon = icon;
	}

	public String getCategory() {
		return category;
	}

	public void setCategory(String category) {
		this.category = category;
	}

	public String getPrice() {
		return price;
	}

	public void setPrice(String price) {
		this.price = price;
	}

	public String getEffect() {
		return effect;
	}

	public void setEffect(String effect) {
		this.effect = effect;
	}

	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}

	public String getSendmessage() {
		return sendmessage;
	}

	public void setSendmessage(String sendmessage) {
		this.sendmessage = sendmessage;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getReceived_beans() {
		return received_beans;
	}

	public void setReceived_beans(String received_beans) {
		this.received_beans = received_beans;
	}

	public String getAlt_price() {
		return alt_price;
	}

	public void setAlt_price(String alt_price) {
		this.alt_price = alt_price;
	}

	public String getBind() {
		return bind;
	}

	public void setBind(String bind) {
		this.bind = bind;
	}

	public String getSort_index() {
		return sort_index;
	}

	public void setSort_index(String sort_index) {
		this.sort_index = sort_index;
	}

	@Override
	public String toString() {
		return "Gift [site=" + site + ", id=" + id + ", icon=" + icon
				+ ", category=" + category + ", price=" + price + ", effect="
				+ effect + ", description=" + description + ", sendmessage="
				+ sendmessage + ", name=" + name + ", received_beans="
				+ received_beans + ", alt_price=" + alt_price + ", bind="
				+ bind + ", sort_index=" + sort_index + "]";
	}

}
