package com.zhipu.chinavideo.entity;

public class GuardViews {
	private String id;
	private String icon;
	private String level;
	private String type;
	private String nickname;
	private String is_year;
	public GuardViews(String id, String icon, String level, String type,
			String nickname, String is_year) {
		super();
		this.id = id;
		this.icon = icon;
		this.level = level;
		this.type = type;
		this.nickname = nickname;
		this.is_year = is_year;
	}
	public GuardViews() {
		super();
	}
	@Override
	public String toString() {
		return "GuardViews [id=" + id + ", icon=" + icon + ", level=" + level
				+ ", type=" + type + ", nickname=" + nickname + ", is_year="
				+ is_year + "]";
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getIcon() {
		return icon;
	}
	public void setIcon(String icon) {
		this.icon = icon;
	}
	public String getLevel() {
		return level;
	}
	public void setLevel(String level) {
		this.level = level;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getNickname() {
		return nickname;
	}
	public void setNickname(String nickname) {
		this.nickname = nickname;
	}
	public String getIs_year() {
		return is_year;
	}
	public void setIs_year(String is_year) {
		this.is_year = is_year;
	}
	
}
