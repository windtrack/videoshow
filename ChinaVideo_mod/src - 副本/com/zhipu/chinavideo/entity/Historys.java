package com.zhipu.chinavideo.entity;

public class Historys {
	
	private String img_url;
	private String name;
	private String lv_url;
	private String time;
	private String room_id;
	public Historys(String img_url, String name, String lv_url, String time,
			String room_id) {
		super();
		this.img_url = img_url;
		this.name = name;
		this.lv_url = lv_url;
		this.time = time;
		this.room_id = room_id;
	}
	public String getImg_url() {
		return img_url;
	}
	public void setImg_url(String img_url) {
		this.img_url = img_url;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getLv_url() {
		return lv_url;
	}
	public void setLv_url(String lv_url) {
		this.lv_url = lv_url;
	}
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}
	public String getRoom_id() {
		return room_id;
	}
	public void setRoom_id(String room_id) {
		this.room_id = room_id;
	}
	@Override
	public String toString() {
		return "Historys [img_url=" + img_url + ", name=" + name + ", lv_url="
				+ lv_url + ", time=" + time + ", room_id=" + room_id + "]";
	}
	
	
	

}
